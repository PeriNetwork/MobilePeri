class PeriUser {
    int nFollowers;
    bool active;
    int id;
    String name;
    String email;
    String password;
    String birthDate;
    DateTime createdAt;

    PeriUser({
        required this.nFollowers,
        required this.active,
        required this.id,
        required this.name,
        required this.email,
        required this.password,
        required this.birthDate,
        required this.createdAt,
    });

}
